SELECT
DISTINCT
TV.NOME AS NOME_DO_VENDEDOR,
TV.BAIRRO AS BAIRRO_DO_VENDEDOR,
TC.NOME AS NOME_DO_CLIENTE,
TC.BAIRRO AS BAIRRO_DO_CLIENTE
FROM
TABELA_DE_VENDEDORES TV
INNER JOIN 
TABELA_DE_CLIENTES TC
ON
TV.BAIRRO = TC.BAIRRO;
--(UTILIZANDO O COMANDO LEFT JOIN)
SELECT
DISTINCT
TV.NOME AS NOME_DO_VENDEDOR,
TV.BAIRRO AS BAIRRO_DO_VENDEDOR,
TC.NOME AS NOME_DO_CLIENTE,
TC.BAIRRO AS BAIRRO_DO_CLIENTE
FROM
TABELA_DE_VENDEDORES TV
LEFT JOIN 
TABELA_DE_CLIENTES TC
ON
TV.BAIRRO = TC.BAIRRO;
-- (UTILIZANDO O COMANDO RIGTH JOIN)
SELECT
DISTINCT
TV.NOME AS NOME_DO_VENDEDOR,
TV.BAIRRO AS BAIRRO_DO_VENDEDOR,
TC.NOME AS NOME_DO_CLIENTE,
TC.BAIRRO AS BAIRRO_DO_CLIENTE
FROM
TABELA_DE_VENDEDORES TV
RIGHT JOIN 
TABELA_DE_CLIENTES TC
ON
TV.BAIRRO = TC.BAIRRO;








