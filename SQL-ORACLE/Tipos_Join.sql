SELECT DISTINCT NOTAS_FISCAIS.MATRICULA
,TABELA_DE_VENDEDORES.NOME , COUNT (*) AS NUMERO_DE_NOTAS
FROM TABELA_DE_VENDEDORES INNER JOIN NOTAS_FISCAIS
ON NOTAS_FISCAIS.MATRICULA = TABELA_DE_VENDEDORES.MATRICULA  -- INNER JOIN SERVE PARA JUNTAR DUAS TABELAS PARA OBTER DADOS (NÃO ESQUECER DE ADICIONAR O NOME NA FRENTE DOS CAMPOS, E NO FINAL ADICIONAR AS CHAVES COMPOSTAS) 
GROUP BY NOTAS_FISCAIS.MATRICULA, TABELA_DE_VENDEDORES. NOME;


