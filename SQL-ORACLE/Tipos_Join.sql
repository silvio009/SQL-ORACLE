TC.NOME AS NOME_DO_CLIENTE,
NF.CPF AS CPF_NOTA_FISCAL
FROM
TABELA_DE_CLIENTES TC
INNER JOIN
NOTAS_FISCAIS NF
ON
TC.CPF = NF.CPF
ORDER BY NOME_DO_CLIENTE;
